import turtle as t
m1=int(input("도형을 몇개 만드시겠습니까? "))
m2=int(input("만들고 싶은 도형은? (x)각형 "))
m3=int(input("각 변의 길이는 얼마로 하시겠습니까? "))
m4=int(input("선의 굵기는 얼마로 하시겠습니까? "))
m5=int(input("채우기 색은 무엇으로 설정하시겠습니까? "))
m6=int(input("배경 색은 무엇으로 설정합니까? "))

t.bgcolor(m6)
for i in range(m1):
	t.clear()
	t.width(m4)
	t.fillcolor(m5)
	t.begin_fill()
	for s1 in range(m2):
		t.fd(m3)
		t.lt(360/m2)
	t.end_fill()